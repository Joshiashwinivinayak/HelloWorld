//Write a program in JAVA to print the result of the specified operations.

public class Exercise {

	public static void main(String[] args) {
		System.out.print("Result of 1st expression is:");
		System.out.println(3 + 5 * 4);
		System.out.print("Result of 2nd expression is :");
		System.out.println((55 + 13) % 9);
		System.out.print("Result of 3rd expression is:");
		System.out.println(7 + -3 * 5 / 3);
		System.out.print("Result of 4th expression is:");
		System.out.println(5 + 15 / 3 * 2 - 12);
	}
}